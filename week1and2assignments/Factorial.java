package week1and2assignments;

public class Factorial {
	public static void main(String[] args) {
		//assigning values
		int fact=1;
		//assigning the integers
		for(int i=1;i<=5;i++) {
			//initiate loop
			fact=fact*i;
			
		}
		//printing the values
		System.out.println(fact);
	}

}
